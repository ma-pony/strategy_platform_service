def seed_reports() -> None:
    """生成 10 篇研报种子数据。"""
    engine = create_engine(settings.database_sync_url)
    now = datetime.datetime.now(tz=datetime.timezone.utc)

    with Session(engine) as session:
        # 检查是否已有数据
        count = session.execute(text("SELECT count(*) FROM research_reports")).scalar()
        if count and count > 0:
            print(f"research_reports 已有 {count} 条数据，跳过种子")
            return

        for report_data in REPORTS:
            generated_at = now - datetime.timedelta(days=report_data["days_ago"])

            # 插入研报
            result = session.execute(
                text(
                    "INSERT INTO research_reports (title, summary, content, generated_at, created_at, updated_at) "
                    "VALUES (:title, :summary, :content, :generated_at, :now, :now) "
                    "RETURNING id"
                ),
                {
                    "title": report_data["title"],
                    "summary": report_data["summary"],
                    "content": report_data["content"],
                    "generated_at": generated_at,
                    "now": now,
                },
            )
            report_id = result.scalar_one()

            # 插入关联币种
            for coin in report_data["coins"]:
                session.execute(
                    text("INSERT INTO report_coins (report_id, coin_symbol) VALUES (:report_id, :coin)"),
                    {"report_id": report_id, "coin": coin},
                )

            print(f"  [+] 研报: {report_data['title'][:30]}... (关联 {len(report_data['coins'])} 个币种)")

        session.commit()
        print(f"\n✅ 共生成 {len(REPORTS)} 篇研报")


if __name__ == "__main__":
    seed_reports()
